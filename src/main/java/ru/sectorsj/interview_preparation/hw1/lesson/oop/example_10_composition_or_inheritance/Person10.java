package ru.sectorsj.interview_preparation.hw1.lesson.oop.example_10_composition_or_inheritance;

public class Person10 {
    String name;
    int age;

    public Person10(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
